function colorChangeGreen() {
    let hintergrund = document.querySelector("body")
    let bgColor = "green"
    let title = document.querySelector("h1")
    let titleColor = "violet"
    hintergrund.style.backgroundColor = bgColor
    title.style.color = titleColor
}

